bool levels::levelCompleted(){
    for(int i = 0; i < levels::rows ; i++){
        for(int j = 0; j < levels::cols ; j++){
            if(currentLevel[i][j] == 2){
                if(round(ballPos::col )== j && round(ballPos::row) == i){
                    signals::grappled = false;
                    ballProp::velocityX = 0;
                    ballProp::velocityY = 0;
                    return true;
                }
            }
        }
    }
    if(energyBar::internal <= 1 && ballProp::velocityX == 0 && ballProp::velocityY == 0){
        signals::grappled = false;
        ballProp::velocityX = 0;
        ballProp::velocityY = 0;
        return true;
    }

    return false;
}



