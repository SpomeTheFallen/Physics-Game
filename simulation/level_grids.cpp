int (*levels::currentLevel)[levels::cols];

void levels::setLevel(levelType type){
    switch(type){
        case levelType::level0:
            currentLevel = level0;
            break;
        case levelType::level1:
            currentLevel = level1;
            break;
        case levelType::level2:
            currentLevel = level2;
            break;
    }
    for(int i = 0 ; i < levels::rows ; i++){
        for(int j = 0 ; j < levels::cols ; j++){
            if(currentLevel[i][j] == 3){
                ballPos::row = i;
                ballPos::col = j;
            }
        }
    }
     
    energyBar::internal = 200;
    for(int i = 0 ; i < 200 ; i+=10){
        if(energyBar::internal < (.9*200 - i)){
            energyBar::bar[(int)(i/(200*.1))] = 0;
        }
        else{
            energyBar::bar[(int)(i/(200*.1))] = 1;
        }
    }
}


bool levels::levelCompleted(){
    for(int i = 0; i < levels::rows ; i++){
        for(int j = 0; j < levels::cols ; j++){
            if(currentLevel[i][j] == 2 || currentLevel[i][j] == 4){
                for(int xy = 0 ; xy < ballProp::rows ; xy++){
                    if(round(ballPos::col+xy) == j && round(ballPos::row+xy) == i){
                        signals::grappled = false;
                        ballProp::velocityX = 0;
                        ballProp::velocityY = 0;
                        return true;
                    }
                }
            }
        }
    }
    if(energyBar::internal <= 1 && ballProp::velocityX == 0 && ballProp::velocityY == 0){
        signals::grappled = false;
        ballProp::velocityX = 0;
        ballProp::velocityY = 0;
        return true;
    }

    return false;
}



